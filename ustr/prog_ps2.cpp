{
  if( serial_acr_mount_ps2() && !have_ps2 )
    {
      if( !prog_copy_to_ram(0x0038, ps2_monitor, sizeof(ps2_monitor)) ) return 0xFFFF;
      have_ps2 = true;
      return 0x0040;
    }
  else
    return 0xffff;
}


static uint16_t ctr = 0;
